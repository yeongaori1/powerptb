const { React, getModule, getAllModules } = require('powercord/webpack');

module.exports = React.memo(() => {
  const divider = getAllModules(m => Object.keys(m).join('') === 'divider', false)[1].divider;
  const dividerDefault = getModule([ 'dividerDefault' ], false).dividerDefault;

  return <div className={`${divider} ${dividerDefault}`}/>;
});
